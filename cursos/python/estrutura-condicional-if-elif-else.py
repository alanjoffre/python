# Estrutura Condicional: if - elif - else

idade = input('Digite sua idade:')

if int(idade) <= 12:
    print('Você é uma criança.')
elif int(idade) <= 18:
    print('Você é um adolescente.')
elif int(idade) <= 29:
    print('Você é um jovem.')          
elif int(idade) <= 40:
    print('Você é um adulto.')
else:
    print('Você é um idoso.') 

















