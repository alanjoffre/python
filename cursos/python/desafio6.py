# criando a lista de números
numeros = [2, 5, 7, 9, 4, 6, 11, 15, 8, 10]

# definindo a variável maior
maior = numeros[0]

# percorrendo a lista para comparar os números
for n in numeros:
    if n > maior:
        maior = n

# exibindo o maior número
print('O maior número da lista é:', maior)


















