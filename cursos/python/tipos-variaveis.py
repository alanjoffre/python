# 4 tipos de varáveis no Python: Integer, Float, String e Boolean

idade = 10 #integer
altura = 1.56 #float
nome = 'joana' #string
casado = False #boolean True ou False

# Python já identifica o tipo de variável, não é necessário atribuir.

print(idade)
print(altura)
print(nome)
print(casado)

# Comando para verificar o tipo de variável: type
print( "Idade é do tipo: ")
print(type(idade))