# Ciclo de Repetição - For

nomes = ['Vitor','Ana', 'Alan']
for nome in nomes:
    print(nome)
    print('=' * 5)

numeros = [4,6,8,1,3]
for numero in numeros:
    print(numero)
print('=' * 5)

for num in range(11):
    print(num)
print('=' * 5)
    
for num in range(1, 11):
    print(num)
print('=' * 5)

for num in range(2, 12, 2):# Pares
    print(num)
print('=' * 5)

for num in range(3, 18, 3):# Impares
    print(num)
print('=' * 5)

for letra in 'Alan':
    print(letra)    
print('=' * 5)




















