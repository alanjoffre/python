# -*- coding: utf-8 -*-

# Método Math - Funções matemáticas

import math

a = 7.3
b = 7.6

print('#' * 30)
print('VALOR DE A : ' + str(a))
print('VALOR DE B : ' + str(b))
print('#' * 30)

print('ROUND')
print(round(a)) # round - arredondamento
print(round(b)) # round - arredondamento
print('#' * 30)

print('CEIL')
print(math.ceil(a)) # ceil - arredonda para cima
print(math.ceil(a)) # ceil - arredonda para cima
print('#' * 30)

print('FLOOR')
print(math.floor(a)) # ceil - arredonda para baixo
print(math.floor(a)) # ceil - arredonda para baixo
print('#' * 30)

print('ABS')
print(abs(a)) # abs - valor absoluto
print(abs(b)) # abs - valor absoluto
print('#' * 30)

print('VALOR DE PI')
print(math.pi)
print('#' * 30)












